#define SUPPORT_FILES _TEXT("All supported files\0*.wav;*.mp3;*.wma;*.ogg\0") \
					  _TEXT("WAV files(*.wav)\0*.wav\0") \
					  _TEXT("MP3 files(*.mp3)\0*.mp3\0") \
					  _TEXT("WMA files(*.wma)\0*.wma\0") \
					  _TEXT("OGG Vorbis files(*.ogg)\0*.ogg\0")

VOID Play(HWND hwnd)
{
	// display open file dialog
	TCHAR szFile[260];
	OPENFILENAME ofn;
	memset(&ofn, 0, sizeof(OPENFILENAME));

	ofn.lStructSize = sizeof(ofn);
	ofn.hwndOwner = hwnd;
	ofn.lpstrFile = szFile;
	ofn.lpstrFile[0] = '\0';
	ofn.nMaxFile = sizeof(szFile);
	ofn.lpstrFilter = SUPPORT_FILES;
	ofn.nFilterIndex = 1;
	ofn.lpstrFileTitle = NULL;
	ofn.nMaxFileTitle = 0;
	ofn.lpstrInitialDir = NULL;
	ofn.Flags = OFN_PATHMUSTEXIST | OFN_FILEMUSTEXIST;

	if (GetOpenFileName(&ofn)==TRUE)
	{
		if(pPlayer != NULL)
		{
			CCriticalSection cs;
			cs.Enter();
			pPlayer->Stop();
			Sleep(200);
			cs.Leave();

			delete pPlayer;
			pPlayer = NULL;
		}


		pPlayer = new CBasicPlayer(hwnd, ofn.lpstrFile);
		if(!pPlayer->GetInput()->GetValid())
			return;

		pPlayer->Start();
	}
}

